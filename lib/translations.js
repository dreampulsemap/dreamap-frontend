export function getTranslation(key, lang = 'en') {
  // Region kodunu temizle (en-US -> en, tr-TR -> tr)
  const baseLang = String(lang).toLowerCase().split('-')[0]
  const language = translations[baseLang] || translations.en
  const fallback = translations.en

  const value = key.split('.').reduce((obj, part) => obj?.[part], language)
  if (value !== undefined && value !== null && value !== '') {
    return value
  }

  const fallbackValue = key.split('.').reduce((obj, part) => obj?.[part], fallback)
  if (fallbackValue !== undefined && fallbackValue !== null && fallbackValue !== '') {
    return fallbackValue
  }

  return key
}

