export const normalizeAddDreamLang = (lang) => {
  if (!lang) return 'en'
  const base = String(lang).toLowerCase().split('-')[0]
  return addDreamTranslations[base] ? base : 'en'
}

export const tAddDream = (key, lang = 'en') => {
  const normalized = normalizeAddDreamLang(lang)
  return addDreamTranslations?.[normalized]?.[key] ?? addDreamTranslations.en[key] ?? key
}
